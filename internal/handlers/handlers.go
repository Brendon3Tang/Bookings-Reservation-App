// Reservation renders the make a reservation page and displays form
func (m *Repository) Reservation(w http.ResponseWriter, r *http.Request) {
	res, ok := m.App.Session.Get(r.Context(), "reservation").(models.Reservation)
	if !ok {
		helpers.ServerError(w, errors.New("cannot get a reservation from session"))
		return
	}

	room, err := m.DB.GetRoomByID(res.RoomID)
	if err != nil {
		helpers.ServerError(w, err)
		return
	}

	res.Room.RoomName = room.RoomName

	m.App.Session.Put(r.Context(), "reservation", res)

	//convert the timd.Time to string with this format
	sd := res.StartDate.Format("2006-01-02")
	ed := res.EndDate.Format("2006-01-02")

	//put the time of string into stringMap and put into the template so that we can use it in tmpl file
	stringMap := make(map[string]string)
	stringMap["start_date"] = sd
	stringMap["end_date"] = ed

	data := make(map[string]interface{})
	data["reservation"] = res //to keep data the clients have written after they submitting and getting error msg,
	//it is important to keep the variable name same with the variable in PostReservation func

	render.Template(w, r, "make-reservation.page.tmpl", &models.TemplateData{
		Form:      forms.New(nil),
		Data:      data,
		StringMap: stringMap,
	})
}

// PostReservation handles the posting of a reservation form
func (m *Repository) PostReservation(w http.ResponseWriter, r *http.Request) {
	reservation, ok := m.App.Session.Get(r.Context(), "reservation").(models.Reservation)
	if !ok {
		helpers.ServerError(w, errors.New("cannot get from session"))
		return
	}

	err := r.ParseForm()
	if err != nil {
		log.Println(err)
		return
	}

	//construct the reservation struct
	reservation.FirstName = r.Form.Get("first_name")
	reservation.LastName = r.Form.Get("last_name")
	reservation.Email = r.Form.Get("email")
	reservation.Phone = r.Form.Get("phone")

	//write the info into database
	newReservationID, err := m.DB.InsertReservation(reservation)
	if err != nil {
		helpers.ServerError(w, err)
		return
	}

	restriction := models.RoomRestriction{
		StartDate:     reservation.StartDate,
		EndDate:       reservation.EndDate,
		RoomID:        reservation.RoomID,
		ReservationID: newReservationID,
		RestrictionID: 1,
	}

	err = m.DB.InsertRoomRestriction(restriction)
	if err != nil {
		helpers.ServerError(w, err)
		return
	}
	//create a form data using the postData
	form := forms.New(r.PostForm)

	//check if the forms are valid. if not, Has func will add a error msg into the map errors
	//form.Has("first_name", r)

	form.Required("first_name", "last_name", "email")

	form.MinLength("first_name", 3, r)

	form.IsEmail("email")

	//check again. if it is not valid, we store the info the clients have input, and render a new web with those info. (so the clients don't lose the info they have written)
	if !form.Valid() {
		data := make(map[string]interface{})
		data["reservation"] = reservation
		render.Template(w, r, "make-reservation.page.tmpl", &models.TemplateData{
			Form: form,
			Data: data,
		})
		return
	}

	//use the seesion to store the info and then when we redirect to the summary page we can take the info out and put it into the new web page
	//don't forget to register the data we want to put into the Session in the main page
	m.App.Session.Put(r.Context(), "reservation", reservation)
	//after we put the data into the session, we redirect the client into a new web page
	http.Redirect(w, r, "/reservation-summary", http.StatusSeeOther)
}

func (m *Repository) ReservationSummary(w http.ResponseWriter, r *http.Request) {
	//the session don't know the type of the data(reservation, in this case) that we store in it, so we need to type assert
	reservation, ok := m.App.Session.Get(r.Context(), "reservation").(models.Reservation) //after the last "." is type assert. If it works, return true to ok
	if !ok {
		log.Println("cannot get item from session")
		m.App.Session.Put(r.Context(), "error", "cannot get reservation from session")
		http.Redirect(w, r, "/", http.StatusTemporaryRedirect)
		return
	}

	//remove the session after using!
	m.App.Session.Remove(r.Context(), "reservation")
	data := make(map[string]interface{})
	data["reservation"] = reservation

	sd := reservation.StartDate.Format("2006-01-02")
	ed := reservation.EndDate.Format("2006-01-02")

	stringMap := make(map[string]string)
	stringMap["start_date"] = sd
	stringMap["end_date"] = ed

	render.Template(w, r, "reservation.page.tmpl", &models.TemplateData{
		Data:      data,
		StringMap: stringMap,
	})
}

